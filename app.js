function escapeHtml(value) {
    return String(value || '')
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#39;');
}

function replaceTokens(template, values) {
    return Object.entries(values).reduce((result, [key, value]) => {
        return result.split(`{${key}}`).join(String(value));
    }, template);
}

function slugify(value, fallback) {
    const slug = String(value || '')
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, '')
        .slice(0, 50);

    return slug || fallback;
}

function clampProgress(value, fallback) {
    const numeric = Number(value);
    if (!Number.isFinite(numeric)) {
        return Math.max(0, Math.min(100, Math.round(fallback)));
    }

    return Math.max(0, Math.min(100, Math.round(numeric)));
}

function normalizeLevel(value) {
    const normalized = String(value || 'B1').trim().toUpperCase();
    if (normalized === 'НЕ ЗНАЮ ТОЧНО') {
        return 'NOT SURE';
    }

    return ENGLISH_PROGRESS_MAP[normalized] ? normalized : 'B1';
}

function buildDefaultPortfolioProgress(englishProgress, motivation) {
    const motivationLength = String(motivation || '').trim().length;
    return Math.min(100, Math.max(36, Math.round((englishProgress * 0.72) + Math.min(16, Math.floor(motivationLength / 16)) + 12)));
}

function buildDefaultMilestone(englishProgress, portfolioProgress) {
    if (englishProgress < 100) {
        return 'Пройти интервью уровня и войти в интенсив по English & Soft Skills.';
    }

    if (portfolioProgress < 85) {
        return 'Собрать 3 сильных кейса и оформить академическую историю для портфолио.';
    }

    return 'Подготовить shortlist колледжей или стажировок и выйти на подачу.';
}

function normalizeAchievement(item, index) {
    if (!item || typeof item !== 'object') {
        return null;
    }

    const title = String(item.title || '').trim();
    if (!title) {
        return null;
    }

    return {
        id: String(item.id || slugify(title, `achievement-${index + 1}`)).trim(),
        title,
        description: String(item.description || '').trim()
    };
}

function normalizeAchievementsCatalog(input) {
    const source = Array.isArray(input) ? input : defaultAchievementsCatalog;
    const result = [];
    const seen = new Set();

    source.forEach((item, index) => {
        const normalized = normalizeAchievement(item, index);
        if (!normalized || seen.has(normalized.id)) {
            return;
        }

        seen.add(normalized.id);
        result.push(normalized);
    });

    return result;
}

function normalizeCertificate(item, index) {
    if (!item || typeof item !== 'object') {
        return null;
    }

    const dataUrl = String(item.dataUrl || '').trim();
    if (!dataUrl.startsWith('data:')) {
        return null;
    }

    return {
        id: String(item.id || `certificate-${index + 1}`).trim(),
        name: String(item.name || `certificate-${index + 1}`).trim(),
        type: String(item.type || 'application/octet-stream').trim(),
        dataUrl,
        uploadedAt: String(item.uploadedAt || new Date().toISOString()).trim() || new Date().toISOString()
    };
}

function normalizeUser(user) {
    if (!user || typeof user !== 'object') {
        return null;
    }

    const email = String(user.email || '').trim().toLowerCase();
    if (!email) {
        return null;
    }

    const englishLevel = normalizeLevel(user.englishLevel);
    const motivation = String(user.motivation || '').trim();
    const englishProgress = clampProgress(user.englishProgress, ENGLISH_PROGRESS_MAP[englishLevel] || ENGLISH_PROGRESS_MAP.B1);
    const portfolioProgress = clampProgress(user.portfolioProgress, buildDefaultPortfolioProgress(englishProgress, motivation));

    return {
        id: String(user.id || `u_${slugify(email, 'student')}`).trim(),
        name: String(user.name || '').trim() || 'Student',
        email,
        englishLevel,
        motivation,
        englishProgress,
        portfolioProgress,
        nextMilestone: String(user.nextMilestone || buildDefaultMilestone(englishProgress, portfolioProgress)).trim()
            || buildDefaultMilestone(englishProgress, portfolioProgress),
        achievementIds: Array.isArray(user.achievementIds)
            ? Array.from(new Set(user.achievementIds.map((item) => String(item || '').trim()).filter(Boolean)))
            : [],
        certificates: Array.isArray(user.certificates)
            ? user.certificates.map((item, index) => normalizeCertificate(item, index)).filter(Boolean)
            : [],
        adminVisiblePassword: String(user.adminVisiblePassword || '').trim(),
        createdAt: String(user.createdAt || '').trim()
    };
}

function normalizeAcademyData(input) {
    const source = input && typeof input === 'object' ? input : defaultAcademyData;
    const intakeInput = source.intake && typeof source.intake === 'object' ? source.intake : {};
    const totalSeats = Number.isFinite(Number(intakeInput.totalSeats))
        ? Math.max(1, Math.round(Number(intakeInput.totalSeats)))
        : defaultAcademyData.intake.totalSeats;
    const remainingSeats = Number.isFinite(Number(intakeInput.remainingSeats))
        ? Math.min(Math.max(0, Math.round(Number(intakeInput.remainingSeats))), totalSeats)
        : defaultAcademyData.intake.remainingSeats;

    return {
        intake: {
            cohortLabel: String(intakeInput.cohortLabel || defaultAcademyData.intake.cohortLabel).trim() || defaultAcademyData.intake.cohortLabel,
            remainingSeats,
            totalSeats
        },
        webinarLink: String(source.webinarLink || '').trim(),
        achievementsCatalog: normalizeAchievementsCatalog(source.achievementsCatalog),
        lessons: Array.isArray(source.lessons) && source.lessons.length ? source.lessons : [...defaultAcademyData.lessons],
        teachers: Array.isArray(source.teachers) && source.teachers.length ? source.teachers : [...defaultAcademyData.teachers],
        homework: Array.isArray(source.homework) && source.homework.length ? source.homework : [...defaultAcademyData.homework]
    };
}

function getSeatWord(count, lang) {
    if (lang === 'en') {
        return count === 1 ? 'spot' : 'spots';
    }

    if (lang === 'ua') {
        const mod10 = count % 10;
        const mod100 = count % 100;
        if (mod10 === 1 && mod100 !== 11) {
            return 'місце';
        }
        if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) {
            return 'місця';
        }
        return 'місць';
    }

    const mod10 = count % 10;
    const mod100 = count % 100;
    if (mod10 === 1 && mod100 !== 11) {
        return 'место';
    }
    if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) {
        return 'места';
    }
    return 'мест';
}

function getLevelLabel(level) {
    return level === 'NOT SURE' ? translations[currentLang].notSureLevel : level;
}

function setCurrentUser(user) {
    const normalized = normalizeUser(user);
    currentUserCache = normalized;

    if (!normalized) {
        localStorage.removeItem('sparkCurrentUser');
        return;
    }

    localStorage.setItem('sparkCurrentUser', JSON.stringify({
        id: normalized.id,
        name: normalized.name,
        email: normalized.email,
        englishLevel: normalized.englishLevel,
        motivation: normalized.motivation,
        englishProgress: normalized.englishProgress,
        portfolioProgress: normalized.portfolioProgress,
        nextMilestone: normalized.nextMilestone,
        achievementIds: normalized.achievementIds
    }));
}

function getStoredUser() {
    try {
        return normalizeUser(JSON.parse(localStorage.getItem('sparkCurrentUser') || 'null'));
    } catch (error) {
        return null;
    }
}

function getCurrentUser() {
    return currentUserCache || getStoredUser();
}

