function readFileAsDataUrl(file) {
    return new Promise((resolve, reject) => {
        const reader = new FileReader();
        reader.onload = () => resolve(reader.result);
        reader.onerror = () => reject(new Error('Не удалось прочитать файл.'));
        reader.readAsDataURL(file);
    });
}

async function handleAdminCertificateAdd() {
    const user = getSelectedAdminUser();
    if (!user) {
        setAdminStatus('Сначала выберите ученика.', true);
        return;
    }

    const input = document.getElementById('adminStudentCertificate');
    const file = input.files && input.files[0];

    if (!file) {
        setAdminStatus('Выберите файл сертификата.', true);
        return;
    }

    if (file.size > CERTIFICATE_UPLOAD_MAX_BYTES) {
        setAdminStatus('Файл слишком большой. Лучше загружать сертификаты до 1.3 MB.', true);
        return;
    }

    try {
        const dataUrl = await readFileAsDataUrl(file);
        const certificate = normalizeCertificate({
            id: `certificate-${Date.now()}-${adminStudentCertificatesDraft.length + 1}`,
            name: file.name,
            type: file.type || 'application/octet-stream',
            dataUrl,
            uploadedAt: new Date().toISOString()
        }, adminStudentCertificatesDraft.length);

        if (!certificate) {
            throw new Error('Не удалось подготовить сертификат для сохранения.');
        }

        adminStudentCertificatesDraft.push(certificate);
        input.value = '';
        renderAdminStudentCertificates();
        setAdminStatus('Сертификат добавлен в черновик ученика. Теперь сохраните ученика.', false);
    } catch (error) {
        setAdminStatus(error.message, true);
    }
}

