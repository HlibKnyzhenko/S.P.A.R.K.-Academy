function buildDashboardModel(user) {
    const englishLevel = normalizeLevel(user?.englishLevel);
    const englishProgress = clampProgress(user?.englishProgress, ENGLISH_PROGRESS_MAP[englishLevel] || ENGLISH_PROGRESS_MAP.B1);
    const portfolioProgress = clampProgress(user?.portfolioProgress, buildDefaultPortfolioProgress(englishProgress, user?.motivation));
    const nextMilestone = String(user?.nextMilestone || buildDefaultMilestone(englishProgress, portfolioProgress)).trim()
        || buildDefaultMilestone(englishProgress, portfolioProgress);
    const achievementMap = new Map((academyDataCache.achievementsCatalog || []).map((item) => [item.id, item]));
    const achievements = Array.isArray(user?.achievementIds)
        ? user.achievementIds.map((id) => achievementMap.get(id)).filter(Boolean)
        : [];
    const certificates = Array.isArray(user?.certificates)
        ? user.certificates.map((item, index) => normalizeCertificate(item, index)).filter(Boolean)
        : [];

    return {
        englishLevel,
        englishProgress,
        portfolioProgress,
        nextMilestone,
        achievements,
        certificates,
        webinarLink: academyDataCache.webinarLink
    };
}

