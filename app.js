document.getElementById('adminStudentForm').addEventListener('submit', async function(event) {
    event.preventDefault();

    const user = getSelectedAdminUser();
    if (!user) {
        setAdminStatus('Сначала выберите ученика.', true);
        return;
    }

    const englishLevel = document.getElementById('adminStudentEnglishLevel').value;
    const englishProgress = Number(document.getElementById('adminStudentEnglishProgress').value);
    const portfolioProgress = Number(document.getElementById('adminStudentPortfolioProgress').value);
    const nextMilestone = document.getElementById('adminStudentNextMilestone').value.trim();
    const newPassword = document.getElementById('adminStudentNewPassword').value.trim();
    const achievementIds = Array.from(document.querySelectorAll('#adminStudentAchievements input[type="checkbox"]:checked'))
        .map((input) => input.value);

    if (!Number.isFinite(englishProgress) || englishProgress < 0 || englishProgress > 100) {
        setAdminStatus('Прогресс по английскому должен быть от 0 до 100.', true);
        return;
    }

    if (!Number.isFinite(portfolioProgress) || portfolioProgress < 0 || portfolioProgress > 100) {
        setAdminStatus('Готовность портфолио должна быть от 0 до 100.', true);
        return;
    }

    if (!nextMilestone) {
        setAdminStatus('Заполните следующий milestone.', true);
        return;
    }

    if (newPassword && newPassword.length < 6) {
        setAdminStatus('Новый пароль должен быть минимум 6 символов.', true);
        return;
    }

    try {
        const updatedUser = await saveAdminStudentRemote(user.id, {
            englishLevel,
            englishProgress,
            portfolioProgress,
            nextMilestone,
            achievementIds,
            certificates: adminStudentCertificatesDraft,
            newPassword
        });

        selectedAdminUserId = updatedUser.id;
        adminStudentCertificatesDraft = updatedUser.certificates.map((item) => ({ ...item }));
        renderAdminState();

        const currentUser = getCurrentUser();
        if (currentUser && currentUser.id === updatedUser.id) {
            setCurrentUser(updatedUser);
        }

        updateDashboard();
        setAdminStatus('Профиль ученика сохранен.', false);
    } catch (error) {
        setAdminStatus(error.message, true);
    }
});

