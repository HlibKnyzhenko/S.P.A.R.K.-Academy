function parseLines(value) {
    return value
        .split('\n')
        .map((line) => line.trim())
        .filter((line) => line.length > 0);
}

function collectAdminGlobalSettingsFromForm() {
    const cohortLabel = document.getElementById('adminCohortLabel').value.trim();
    const remainingSeats = Number(document.getElementById('adminRemainingSeats').value);
    const totalSeats = Number(document.getElementById('adminTotalSeats').value);
    const webinarLink = document.getElementById('adminWebinarLink').value.trim();
    const lessons = parseLines(document.getElementById('adminLessons').value);
    const teachers = parseLines(document.getElementById('adminTeachers').value);
    const homework = parseLines(document.getElementById('adminHomework').value);

    if (!cohortLabel || !Number.isFinite(remainingSeats) || !Number.isFinite(totalSeats)) {
        throw new Error('Заполните блок потока и количество мест.');
    }

    if (remainingSeats < 0 || totalSeats < 1 || remainingSeats > totalSeats) {
        throw new Error('Проверьте значения мест: остаток не может быть больше общего числа.');
    }

    if (!lessons.length || !teachers.length || !homework.length) {
        throw new Error('Заполните уроки, учителей и домашние задания минимум одной строкой.');
    }

    return {
        intake: {
            cohortLabel,
            remainingSeats,
            totalSeats
        },
        webinarLink,
        achievementsCatalog: academyDataCache.achievementsCatalog,
        lessons,
        teachers,
        homework
    };
}

